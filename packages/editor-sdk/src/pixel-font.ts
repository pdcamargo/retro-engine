/** Width in px of `text` rendered at pixel scale `px` (5-wide glyphs + 1-col gap). */
export const pixelTextWidth = (text: string, px: number): number =>
  text.length === 0 ? 0 : text.length * 6 * px - px;

/**
 * Draw `text` (uppercased) as a crisp 5×7 pixel font at the given pixel scale,
 * top-left at `min`. Returns the rendered width in px. Unknown characters render
 * as blanks. Positions are integer-snapped so the pixels stay sharp.
 */
export const drawPixelText = (min: Vec2, text: string, px: number, col: number): number => {
  const dl = Draw.window();
  const x0 = Math.round(min[0]);
  const y0 = Math.round(min[1]);
  let cx = x0;
  for (const ch of text.toUpperCase()) {
    const glyph = GLYPHS[ch];
    if (glyph !== undefined) {
      for (let r = 0; r < glyph.length; r++) {
        const row = glyph[r];
        if (row === undefined) continue;
        for (let c = 0; c < row.length; c++) {
          if (row[c] !== '#') continue;
          const x = cx + c * px;
          const y = y0 + r * px;
          dl.rectFilled([x, y], [x + px, y + px], col);
        }
      }
    }
    cx += 6 * px;
  }
  return text.length * 6 * px - px;
};
